package com.truckload.core.entity;

public class Trucks {

	private String file;
	private String filename;
	private char separartor;
	private boolean titles;
	private String email;

	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public char getSeparartor() {
		return separartor;
	}
	public void setSeparartor(char separartor) {
		this.separartor = separartor;
	}

	public boolean isTitles() {
		return titles;
	}
	public void setTitles(boolean titles) {
		this.titles = titles;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
	
}
